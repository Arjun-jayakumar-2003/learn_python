character_name = "Tom"
character_age = "50"
""" Rather than writing names and numerals everytime we want to mention them we can use variables to store 
the necessary values then write just the variables when the values are needed to be referenced """
print("There once was a man named " + character_name + ", ")
print("he was " + character_age + " years old. ")

character_name = "Mike"
""" Variables values can be changed which would mean that from that point onwards when that variable is 
mentioned the new value will be referenced and used  """
print("He really liked the name " + character_name + ", ")
print("but didn't like being " + character_age + ".")
